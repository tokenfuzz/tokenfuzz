# ─── Threat-model seed lookup (reads .agents/references/threat_models.toml) ──
#
# The per-target threat-model seed table is operator-curated reference data,
# not shared-harness logic — keeping a per-project dict literal in lib/ would
# violate the target-agnostic rule. It lives in .agents/references/, the same
# home as the S6 peers table, and is read at seed time. Unknown non-browser
# targets fall back to the conservative byte-only default; browser targets
# share one structural threat model keyed off is_browser (not a project list).

def _threat_models_toml_path() -> Path:
    """Resolve .agents/references/threat_models.toml relative to this file."""
    return (
        Path(__file__).resolve().parent.parent
        / ".agents" / "references" / "threat_models.toml"
    )


def threat_model_for(slug: str,
                     threat_models_path: Path | None = None) -> list[str]:
    """Return the curated default attacker_controls for `slug`, or [] if none.

    Reads .agents/references/threat_models.toml (or the override path for
    testing). An empty list means the slug has no curated entry — the caller
    falls back to the byte-only default. Malformed/missing file degrades to
    [] so seeding still proceeds.
    """
    path = threat_models_path if threat_models_path is not None else _threat_models_toml_path()
    if not path.is_file():
        return []
    try:
        parsed = parse_toml(str(path))
    except Exception:
        return []
    entry = parsed.get(slug)
    if not isinstance(entry, dict):
        return []
    controls = entry.get("attacker_controls", [])
    if not isinstance(controls, list):
        return []
    return [c for c in controls if isinstance(c, str) and c]


def attacker_controls_for_seed(slug: str, is_browser: bool,
                               threat_models_path: Path | None = None) -> list[str]:
    """Return default [threat_model].attacker_controls for a seeded target.

    Browser targets share one structural threat model. Non-browser targets
    use the operator-curated seed from .agents/references/threat_models.toml,
    falling back to the conservative byte-only default when no entry exists.
    """
    if is_browser:
        return ["bytes", "call-sequence", "timing"]
    curated = threat_model_for(slug, threat_models_path)
    return curated if curated else ["bytes"]


# Runtime symbol each sanitizer's instrumentation links into an executable.
# `nm` reveals them; their presence confirms the binary is actually
# instrumented (not a stray host tool that landed in the build tree).
_SANITIZER_RUNTIME_MARKER = {
    "asan": "__asan_", "ubsan": "__ubsan_", "msan": "__msan_", "tsan": "__tsan_",
}


def _binary_uses_sanitizer(path: Path, sanitizer: str = "asan") -> bool:
    marker = _SANITIZER_RUNTIME_MARKER.get(sanitizer, "__asan_")
    try:
        out = subprocess.run(
            ["nm", str(path)], capture_output=True, text=True, timeout=20,
        )
    except (OSError, subprocess.TimeoutExpired):
        return False
    if out.returncode != 0:
        return False
    return marker in out.stdout


def _find_under(asan_dir: Path, *, name: str | None = None,
                maxdepth: int = 3) -> list[Path]:
    """Equivalent to: find <asan_dir> -maxdepth <n> -type f [-name <name>]."""
    if not asan_dir.is_dir():
        return []
    out: list[Path] = []

    def walk(d: Path, depth: int) -> None:
        if depth > maxdepth:
            return
        try:
            entries = sorted(d.iterdir())
        except OSError:
            return
        for e in entries:
            if e.is_file():
                if name is None or e.name == name:
                    out.append(e)
            elif e.is_dir() and not e.is_symlink():
                walk(e, depth + 1)

    walk(asan_dir, 1)
    return out


def _is_shared_lib(name: str) -> bool:
    """A linkable shared object: lib<name>.so / .dylib, or a versioned
    SONAME (lib<name>.so.2, lib<name>.2.dylib)."""
    return name.endswith((".so", ".dylib")) or ".so." in name


# Build subdirectories that hold artifacts which are never the audited
# project's own library or CLI: CMake's CMakeFiles/ (compiler-probe
# binaries like CMakeDetermineCompilerABI_C.bin, object trees), unit-test
# frameworks vendored under test(s)/ (Unity, gtest, …), and FetchContent
# dependency builds under _deps/. Inclusion criterion: a directory a build
# system populates with helper/probe/dependency artifacts, not the target's
# primary output. Structural (no project names), target-agnostic as new
# projects appear.
_AUX_BUILD_DIRS = {"cmakefiles", "test", "tests", "_deps"}


def _is_aux_build_path(p: Path, base_dir: Path) -> bool:
    try:
        rel = p.relative_to(base_dir)
    except ValueError:
        return False
    return any(part.lower() in _AUX_BUILD_DIRS for part in rel.parts[:-1])


def _path_has_aux_component(rel_path: str) -> bool:
    """True if a target.toml-relative path string traverses a build-internal
    aux directory — used to recognise a stale/bogus <san>_bin/<san>_lib value
    (e.g. a CMakeFiles compiler probe) that detection should scrub."""
    return any(part.lower() in _AUX_BUILD_DIRS
               for part in rel_path.replace("\\", "/").split("/"))


def _pick_shared_lib(san_dir: Path, root: Path) -> str:
    """Best shared library to link a C harness against, when the build
    emits no static archive (the cmake/meson default for c-ares, pcre2,
    …). Prefer the canonical unversioned linker name (libfoo.so,
    libfoo.dylib) over its versioned SONAME siblings (libfoo.so.2,
    libfoo.2.19.4.dylib); among equals, shortest basename then path.

    Single-library convention, mirroring the static-archive heuristic
    above: ambiguous multi-library builds resolve to the first canonical
    match, which the operator can override in target.toml."""
    shared = [s for s in _find_under(san_dir, name=None)
              if _is_shared_lib(s.name) and "posix" not in s.name
              and not _is_aux_build_path(s, san_dir)]
    if not shared:
        return ""
    shared.sort(key=lambda s: (not s.name.endswith((".so", ".dylib")),
                               len(s.name), str(s)))
    return str(shared[0]).removeprefix(str(root) + "/")


def _detect_sanitizer_lib(san_dir: Path, root: Path) -> str:
    """Library a C harness must link against for the sanitizer built under
    `san_dir` (a build-<san>/ tree). Static archive preferred (the harness
    links it directly); otherwise the canonical instrumented shared object.
    Empty when the build tree is absent or ships no linkable library —
    i.e. a header-only library or a CLI-only target. Sanitizer-agnostic:
    the same archive-then-shared rule holds for asan/ubsan/msan/tsan."""
    if not san_dir.is_dir():
        return ""
    archives = [a for a in _find_under(san_dir, name=None)
                if a.suffix == ".a" and not _is_aux_build_path(a, san_dir)]
    for a in archives[:3]:
        if "posix" in a.name:
            continue
        return str(a).removeprefix(str(root) + "/")
    if archives:
        return str(archives[0]).removeprefix(str(root) + "/")
    return _pick_shared_lib(san_dir, root)


def _detect_cli_bin(san_dir: Path, root: Path, build_system: str,
                    sanitizer: str = "asan") -> str:
    """Best instrumented CLI executable under `san_dir` (a build-<san>/
    tree): a declared CLI name from the build manifests first, else the
    first instrumented executable that is not a shared library, object file,
    or build-system-internal artifact (CMakeFiles/ compiler probes, test
    helpers). Empty when the build ships no CLI — a library-only or
    header-only target. The aux-dir prune is what stops a CMake compiler
    probe (CMakeDetermineCompilerABI_C.bin) being mistaken for the tool."""
    if not san_dir.is_dir():
        return ""
    for cand_name in declared_cli_names(root, build_system):
        for m in _find_under(san_dir, name=cand_name):
            if (os.access(m, os.X_OK) and not _is_aux_build_path(m, san_dir)
                    and _binary_uses_sanitizer(m, sanitizer)):
                return str(m).removeprefix(str(root) + "/")
    # Filter to plausible CLI executables BEFORE capping the nm probes:
    # CMakeFiles/ alone can hold dozens of files, so capping first would
    # exhaust the budget on pruned entries and never reach the real tool
    # (e.g. curl's build puts src/curl after a large CMakeFiles/ tree).
    def _is_cli_candidate(f: Path) -> bool:
        if _is_aux_build_path(f, san_dir) or not os.access(f, os.X_OK):
            return False
        return not (f.name.endswith((".exe", ".so", ".dylib", ".o"))
                    or "sanity" in f.name)
    candidates = [f for f in _find_under(san_dir) if _is_cli_candidate(f)]
    for f in candidates[:60]:
        if _binary_uses_sanitizer(f, sanitizer):
            return str(f).removeprefix(str(root) + "/")
    return ""


def refresh_detected_build_fields(target_root: Path, toml_path: Path) -> bool:
    """Re-detect <san>_bin and <san>_lib for every sanitizer from the build
    trees now on disk and correct them in target.toml in place. Policy per
    field: when the build yields a value, adopt it (filling an unset field
    or replacing a stale one); when it does not, scrub a value that points
    at a build-internal artifact (a CMake compiler probe, a test-framework
    archive) or a now-missing path, and otherwise leave the field — and
    every curated section — untouched. Returns True if anything changed.

    seed_toml must run before any build exists (it supplies build_system to
    the build step), so on a fresh target it cannot detect these fields and
    leaves them commented FILL_ME; on a target rebuilt under different cmake
    options a previously-detected path can also go stale. setup-target
    calls this after materializing the canonical build to repair that
    ordering — a surgical line edit, not a re-seed, so the LLM-curated
    [threat_model]/[s6_peers] survive."""
    if target_root.name in _BROWSER_SLUGS:
        return False  # browsers use fixed bin candidates; no harness lib link
    suffix = os.environ.get("AUDIT_BUILD_SUFFIX", "")
    build_system = _detect_build_system(target_root)
    try:
        lines = Path(toml_path).read_text(encoding="utf-8").splitlines()
    except OSError:
        return False
    changed = False

    def _value_of(line: str) -> str:
        if line.lstrip().startswith("#"):
            return ""
        m = re.search(r'=\s*"((?:[^"\\]|\\.)*)"', line)
        return m.group(1) if m else ""

    def _active(field: str, value: str) -> str:
        # asan_* keep seed's wide alignment; the rest use the [sanitizer]
        # block's short form. Cosmetic — the TOML parser ignores spacing.
        if field in ("asan_bin", "asan_lib"):
            return f"{field}{' ' * (14 - len(field))}= {toml_basic_string(value)}"
        pad = " " * (len("ubsan_lib") - len(field))
        return f"{field}{pad} = {toml_basic_string(value)}"

    def _commented(field: str, san: str) -> str:
        # Retain FILL_ME so bin/setup-target's grep-for-FILL_ME re-seed
        # trigger keeps firing for the now-unset field.
        stem = "FILL_ME.a" if field.endswith("_lib") else "FILL_ME"
        return f'# {field} = "build-{san}{suffix}/{stem}"'

    def _refresh(field: str, san: str, detected: str) -> None:
        nonlocal changed
        field_re = re.compile(rf"^\s*#?\s*{field}\b\s*=")
        for i, ln in enumerate(lines):
            if not field_re.match(ln):
                continue
            cur = _value_of(ln)
            cur_bogus = bool(cur) and (
                _path_has_aux_component(cur)
                or not (target_root / cur).exists()
            )
            if detected:
                new_line = _active(field, detected)      # fill or correct
            elif cur_bogus:
                new_line = _commented(field, san)        # scrub stale/bogus
            else:
                return                                   # leave good or unset
            if new_line != ln:
                lines[i] = new_line
                changed = True
            return

    for san in ("asan", "ubsan", "msan", "tsan"):
        san_dir = target_root / f"build-{san}{suffix}"
        _refresh(f"{san}_lib", san, _detect_sanitizer_lib(san_dir, target_root))
        _refresh(f"{san}_bin", san,
                 _detect_cli_bin(san_dir, target_root, build_system, san))
    if changed:
        Path(toml_path).write_text("\n".join(lines) + "\n", encoding="utf-8")
    return changed


_HEADER_SUFFIXES = (".h", ".hpp", ".hh", ".hxx", ".H")


def _detect_include_dirs(target_root: Path, asan_dir_name: str) -> list[str]:
    """Return public-header search paths relative to target_root.

    Heuristic, not exhaustive — covers the layouts the seeder has observed:
      - `include/` populated with headers (curl, libxml2, openssl, ...)
      - headers at source root (cJSON, sqlite, lua, libpng, ...)
      - `src/` carrying public headers (some autotools projects)
    The `build-asan/include` entry is always emitted because CMake/Meson
    routinely generate config headers there; if the dir doesn't exist at
    repro time the -I just does nothing.
    """
    out: list[str] = []

    def _has_headers(d: Path, recursive: bool = False) -> bool:
        if not d.is_dir():
            return False
        try:
            it = d.rglob("*") if recursive else d.iterdir()
            for e in it:
                if e.is_file() and e.suffix in _HEADER_SUFFIXES:
                    return True
        except OSError:
            pass
        return False

    if _has_headers(target_root / "include", recursive=True):
        out.append("include")
    if _has_headers(target_root, recursive=False):
        out.append(".")
    # `src/` is a public-header location for some autotools projects but a
    # private-only one for many others; only include it when it actually
    # has headers AND no `include/` was found, to avoid pulling private
    # internals onto the search path for projects that already publish via
    # `include/`.
    if "include" not in out and _has_headers(target_root / "src", recursive=False):
        out.append("src")
    out.append(f"{asan_dir_name}/include")
    return out


def _detect_build_system(target_root: Path) -> str:
    """Return a slug naming the dominant build/package system at target_root.

    The slug drives both `seed_toml`'s default [runner] block and
    `bin/export-repro`'s reproduce.sh template. Order matters: native-build
    systems are checked before language-ecosystem package managers because
    a polyglot tree may have Cargo+autotools (e.g. a Rust binding inside a
    C library) — the native build is the one ASan ties into.
    """
    # Native compiled systems (driver scripts first, then build files).
    if (target_root / "mach").is_file() and os.access(target_root / "mach", os.X_OK):
        return "mach"
    if (target_root / "CMakeLists.txt").is_file():
        return "cmake"
    if (target_root / "meson.build").is_file():
        return "meson"
    if (
        (target_root / "configure").is_file() and os.access(target_root / "configure", os.X_OK)
        or (target_root / "configure.ac").is_file()
        or (target_root / "autogen.sh").is_file()
    ):
        return "autotools"
    # Language ecosystems — single canonical manifest per language. The
    # order here matters only when a tree has multiple manifests; the
    # first match wins. We pick the language whose runtime is most
    # likely to drive testcases on this target.
    if (target_root / "Cargo.toml").is_file():
        return "cargo"
    if (target_root / "go.mod").is_file():
        return "go"
    if (target_root / "Package.swift").is_file():
        return "swift"
    if (target_root / "pom.xml").is_file():
        return "maven"
    if (
        (target_root / "build.gradle").is_file()
        or (target_root / "build.gradle.kts").is_file()
        or (target_root / "settings.gradle").is_file()
        or (target_root / "settings.gradle.kts").is_file()
    ):
        return "gradle"
    if (
        any(target_root.glob("*.kts"))
        or any(target_root.glob("*.kt"))
        or any(target_root.glob("src/**/*.kt"))
        or any(target_root.glob("src/**/*.kts"))
    ):
        return "kotlin"
    if (
        (target_root / "pyproject.toml").is_file()
        or (target_root / "setup.py").is_file()
        or (target_root / "setup.cfg").is_file()
    ):
        return "python"
    if (target_root / "package.json").is_file():
        return "npm"
    if (target_root / "Gemfile").is_file() or any(target_root.glob("*.gemspec")):
        return "bundler"
    if (target_root / "composer.json").is_file():
        return "composer"
    if (target_root / "DESCRIPTION").is_file():
        return "rlang"
    if (target_root / "Makefile.PL").is_file() or any(target_root.glob("*.pm")):
        return "perl"
    return ""


# ─── Per-build-system runner defaults ───────────────────────────────
#
# When seed_toml writes a target.toml for a tree with no ASan binary
# detected, it also emits a starter [runner] block driven by the
# detected build system. The mapping below names the interpreter or
# driver plus its argument template; "{TESTCASE}" is substituted at
# run time. crash_patterns are language-specific runtime diagnostic
# strings (panics, tracebacks, race-detector reports) that lib/triage.sh
# treats as crash signals.
#
# This table is derived from the language registry in lib/languages.py.
# To add a new language or build_system, edit lib/languages.py — the
# registry is the single source of truth. Tests assert this dict and
# languages.runner_table() stay in sync so the silo cannot drift.
LANGUAGE_RUNNERS: dict[str, dict] = languages.runner_table()


def _java_home_candidates(home: str) -> list[Path]:
    base = Path(home)
    return [base / "bin" / "java", base / "Contents" / "Home" / "bin" / "java"]


def _java_home_for_bin(java_bin: Path) -> str:
    # .../Contents/Home/bin/java -> .../Contents/Home
    # .../bin/java               -> ...
    if java_bin.parent.name == "bin":
        return str(java_bin.parent.parent)
    return ""


def _java_is_usable(java_bin: Path) -> bool:
    if not java_bin.is_file() or not os.access(java_bin, os.X_OK):
        return False
    try:
        result = subprocess.run(
            [str(java_bin), "-version"],
            stdout=subprocess.DEVNULL,
            stderr=subprocess.DEVNULL,
            timeout=10,
        )
    except (OSError, subprocess.TimeoutExpired):
        return False
    return result.returncode == 0


def _detect_java_runner() -> tuple[str, list[str]]:
    """Return a usable Java executable and env entries, if one is discoverable."""
    for env_name in ("AUDIT_JAVA_HOME", "JAVA_HOME"):
        java_home = os.environ.get(env_name, "")
        if not java_home:
            continue
        for candidate in _java_home_candidates(java_home):
            if _java_is_usable(candidate):
                home = _java_home_for_bin(candidate)
                return str(candidate), ([f"JAVA_HOME={home}"] if home else [])

    path_java = shutil.which("java")
    if path_java:
        candidate = Path(path_java)
        if _java_is_usable(candidate):
            home = _java_home_for_bin(candidate)
            return str(candidate), ([f"JAVA_HOME={home}"] if home else [])

    return "", []


# ─── Build freshness (audit-owned lazy build) ───────────────────────
#
# bin/audit and bin/benchmark are the consumption points: they (re)build a
# stale or missing native sanitizer tree on demand so a checkout that moved
# since the last build is never audited against the wrong binary. These two
# helpers are the cheap probe and the stamp those callers and `setup-target
# --build` share. Native C/C++ only — language targets (cargo/go/pip) and
# browsers build elsewhere and report "skip".
_NATIVE_BUILD_SYSTEMS = {"cmake", "autotools", "meson"}
# VCS metadata and generated caches — never source whose change should force a
# rebuild. Pruned from the mtime walk so a fresh .audit log or __pycache__ entry
# can't masquerade as a source edit. (The sanitizer build trees themselves are
# pruned separately, by exact name — see san_build_dirs in build_freshness.)
_FRESHNESS_PRUNE_DIRS = {".git", ".hg", ".audit", "__pycache__", "node_modules"}


def _build_dir_for(target_root: Path, san: str) -> Path:
    suffix = os.environ.get("AUDIT_BUILD_SUFFIX", "")
    return Path(target_root) / f"build-{san}{suffix}"


def _source_state_signature(root: Path) -> str:
    """Hash the (relative path, mtime) of every source file under root.

    Compared across a build, a change in this signature flags ANY source-tree
    edit — modification or addition (an mtime/path appears), AND deletion or
    rename (a path disappears), which a bare max-mtime check misses because no
    remaining file is newer. Prunes VCS/cache dirs and only the canonical
    sanitizer build trees by exact name, so a sibling build compiled later (or
    this san's own tree) is excluded, while a source-support dir like build-aux/
    is still hashed. Errs toward "changed": any touch flips it (a harmless
    rebuild), but nothing real slips through as unchanged."""
    suffix = os.environ.get("AUDIT_BUILD_SUFFIX", "")
    san_build_dirs = {
        f"build-{s}{sfx}"
        for s in ("asan", "ubsan", "msan", "tsan")
        for sfx in ("", suffix)
    }
    entries: list[str] = []
    for dirpath, dirnames, filenames in os.walk(root):
        dirnames[:] = [
            d for d in dirnames
            if d not in _FRESHNESS_PRUNE_DIRS and d not in san_build_dirs
        ]
        for fn in filenames:
            fp = Path(dirpath) / fn
            try:
                mt = fp.stat().st_mtime
            except OSError:
                continue  # races/broken symlinks: not a staleness signal
            entries.append(f"{os.path.relpath(fp, root)}\0{mt:.3f}")
    entries.sort()
    return hashlib.sha1(
        "\n".join(entries).encode("utf-8", "surrogatepass")
    ).hexdigest()


def build_write_stamp(target_root: "str | os.PathLike", san: str) -> bool:
    """Mark build-<san> as current with the source tree as of now.

    Writes build-<san>/.audit-build-stamp with the detected rev (line 1, for
    human diagnostics) and a source-state signature (line 2) that build_freshness
    recomputes and compares. Returns False (no-op) when the build dir does not
    exist."""
    root = Path(target_root)
    build_dir = _build_dir_for(root, san)
    if not build_dir.is_dir():
        return False
    rev = detect_rev(root) or ""
    sig = _source_state_signature(root)
    (build_dir / ".audit-build-stamp").write_text(
        f"{rev}\n{sig}\n", encoding="utf-8")
    return True


def build_freshness(target_root: "str | os.PathLike", san: str = "asan") -> str:
    """Classify build-<san> against the current source tree.

    Returns one of:
      * "skip"    — browser, or not a native C/C++ build system: no
                    build-<san> tree is expected, so freshness is N/A and
                    the caller must not try to (re)build one.
      * "missing" — native target with no build-<san> tree yet.
      * "stale"   — the source-state signature differs from the recorded
                    stamp (an upstream pull, branch switch, or local edit,
                    add, delete, or rename since the build), or the stamp
                    predates this mechanism.
      * "fresh"   — build-<san> exists and the source signature still matches.

    The signature (see _source_state_signature) catches every update mode
    uniformly — VCS or not, including deletion-only changes — and errs toward
    "stale" (a needless rebuild), never toward "fresh", which would audit the
    wrong binary."""
    root = Path(target_root)
    if root.name in _BROWSER_SLUGS:
        return "skip"
    if _detect_build_system(root) not in _NATIVE_BUILD_SYSTEMS:
        return "skip"
    build_dir = _build_dir_for(root, san)
    if not build_dir.is_dir():
        return "missing"
    stamp = build_dir / ".audit-build-stamp"
    if not stamp.exists():
        return "stale"  # built before stamping existed → rebuild to be safe
    try:
        stamp_lines = stamp.read_text(encoding="utf-8").splitlines()
    except OSError:
        return "stale"
    stored_sig = stamp_lines[1] if len(stamp_lines) >= 2 else ""
    if not stored_sig:
        return "stale"  # old-format (rev-only) stamp → rebuild to upgrade it
    return "fresh" if _source_state_signature(root) == stored_sig else "stale"


def language_runner_defaults(build_system: str) -> dict:
    """Return the default [runner] block for a build_system, or {}."""
    defaults = {k: (list(v) if isinstance(v, list) else v)
                for k, v in LANGUAGE_RUNNERS.get(build_system, {}).items()}
    if build_system in {"maven", "gradle", "kotlin"} and defaults:
        java_bin, java_env = _detect_java_runner()
        if java_bin:
            if build_system in {"maven", "gradle"}:
                defaults["bin"] = java_bin
            defaults["env"] = java_env
    return defaults


def _detect_upstream_url(target_root: Path) -> str:
    if (target_root / ".git").is_dir():
        try:
            out = subprocess.run(
                ["git", "-C", str(target_root), "config", "--get", "remote.origin.url"],
                capture_output=True, text=True, timeout=10,
            )
            if out.returncode == 0:
                return out.stdout.strip()
        except (OSError, subprocess.TimeoutExpired):
            pass
    if (target_root / ".hg").is_dir():
        try:
            out = subprocess.run(
                ["hg", "--cwd", str(target_root), "paths", "default"],
                capture_output=True, text=True, timeout=10,
            )
            if out.returncode == 0:
                return out.stdout.strip()
        except (OSError, subprocess.TimeoutExpired):
            pass
    return ""


def seed_toml(
    target_root: str | os.PathLike,
    out_path: str | os.PathLike,
    upstream_url: str = "",
    preserve_curated: bool = False,
) -> None:
    """Seed a starter target.toml — best-effort introspection.

    When ``preserve_curated`` is set and out_path already parses, the curated
    sections a fresh seed cannot re-derive — [threat_model].attacker_controls
    and the whole [s6_peers] block — are carried over from the existing file
    instead of reset to the conservative default (attacker_controls) or dropped
    (s6_peers, which a plain seed never emits). bin/setup-target uses this for a
    placeholder refresh, so filling one unrelated FILL_ME never discards an
    operator's hand- or LLM-curated threat model / peer set. A full re-seed
    (--force, missing, or unparseable file) leaves it False and regenerates."""
    root = Path(target_root)
    out = Path(out_path)
    slug = root.name
    is_browser = slug in _BROWSER_SLUGS

    # Read the curated sections to carry forward BEFORE the template is built,
    # so the render below can substitute them in place of the seed defaults.
    preserved_controls: Optional[list[str]] = None
    preserved_s6: Optional[tuple[str, list[str]]] = None
    if preserve_curated and out.exists():
        try:
            _existing = parse_toml(out)
        except Exception:
            _existing = {}
        _tm = _existing.get("threat_model")
        if isinstance(_tm, dict):
            _ac = _tm.get("attacker_controls")
            if isinstance(_ac, list):
                _ac = [str(x) for x in _ac if isinstance(x, str) and x]
                if _ac:
                    preserved_controls = _ac
        _s6 = _existing.get("s6_peers")
        if isinstance(_s6, dict):
            _peers = _s6.get("peers")
            if isinstance(_peers, list):
                _peers = [str(p) for p in _peers if isinstance(p, str) and p]
                if _peers:
                    _dom = _s6.get("domain")
                    preserved_s6 = (
                        str(_dom) if isinstance(_dom, str) else "",
                        _peers,
                    )
    # Detected up front: declared_cli_names() needs the build system to know
    # which manifest to read when biasing the asan_bin guess below.
    build_system = _detect_build_system(root)

    asan_bin = ""
    asan_lib = ""

    # AUDIT_BUILD_SUFFIX is set by bin/audit-container-shell so each
    # container image gets its own build tree; empty outside.
    suffix = os.environ.get("AUDIT_BUILD_SUFFIX", "")
    def _build_dir_name(san: str) -> str:
        return f"build-{san}{suffix}"

    asan_dir = root / _build_dir_name("asan")

    if is_browser:
        for cand in _BROWSER_BIN_CANDIDATES:
            full = asan_dir / cand
            if full.is_file() and os.access(full, os.X_OK):
                asan_bin = f"{_build_dir_name('asan')}/{cand}"
                break
    elif asan_dir.is_dir():
        # Candidate CLI names come from the target's own build manifests
        # (declared_cli_names), not a hardcoded per-project table. An
        # empty list just means we fall through to the free scan below.
        asan_bin = _detect_cli_bin(asan_dir, root, build_system, "asan")
        asan_lib = _detect_sanitizer_lib(asan_dir, root)

    if not upstream_url:
        upstream_url = _detect_upstream_url(root)
    pinned_rev = detect_rev(root)

    out.parent.mkdir(parents=True, exist_ok=True)

    upstream_v = upstream_url or "FILL_ME"
    build_system_v = build_system or "unknown"
    pinned_v = pinned_rev or "HEAD"

    # When introspection finds no ASan binary or archive, emit the field
    # commented out rather than as a "FILL_ME" placeholder string. A literal
    # placeholder is indistinguishable from a real path to downstream tools
    # (bin/export-repro would copy "build-asan/FILL_ME.a" into reproduce.sh
    # verbatim). The "FILL_ME" token stays in the comment so bin/setup-target's
    # grep-for-FILL_ME refresh trigger still fires, and so a human reviewer can
    # see at a glance that the field is unset. Header-only C++ libraries
    # legitimately have no asan_lib; the commented line is the steady state
    # for those targets.
    _asan_dir_name = _build_dir_name("asan")
    asan_bin_line = (
        f"asan_bin      = {toml_basic_string(asan_bin)}" if asan_bin
        else f'# asan_bin    = "{_asan_dir_name}/FILL_ME"      '
             '# uncomment + fill: ASan-instrumented CLI used by bin/run-asan'
    )
    asan_lib_line = (
        f"asan_lib      = {toml_basic_string(asan_lib)}" if asan_lib
        else f'# asan_lib    = "{_asan_dir_name}/FILL_ME.a"    '
             '# uncomment + fill if a // HARNESS testcase links a static archive'
    )

    lines = [
        "# target.toml — generated by bin/setup-target or bin/audit.",
        "# Review generated values; edit only unresolved or target-specific fields.",
        "# Paths are relative to target_root unless absolute.",
        "",
        f"target        = {toml_basic_string(slug)}",
        f"upstream_url  = {toml_basic_string(upstream_v)}",
        f"build_system  = {toml_basic_string(build_system_v)}",
        f"pinned_rev    = {toml_basic_string(pinned_v)}",
        "",
        "# Direct CLI binary — what `bin/run-asan generic` runs by default:",
        asan_bin_line,
        "",
        "# C-API harness build inputs (testcases with // HARNESS: link against this):",
        asan_lib_line,
        'includes      = [' + ", ".join(
            toml_basic_string(inc) for inc in _detect_include_dirs(root, _asan_dir_name)
        ) + "]",
        'link_libs     = ["-lm", "-lpthread"]',
        "",
        "# Optional: cmake target name used by maintainer reproduce.sh:",
        '# cmake_target  = "xmlcatalog"',
        "",
        "# Browser target? (1 enables browser/js mode, 0 = generic)",
        f'is_browser    = "{1 if is_browser else 0}"',
        "",
        "# ── Threat model (drives lib/triage.sh verdict matrix) ──────────",
        "# attacker_controls lists what an attacker can supply to the target.",
        "# A crash report's Trigger source: must be a subset of this set, or",
        "# triage demotes it from security to robustness. Tokens:",
        "#   bytes           input file/stream/packet bytes (most parsers)",
        "#   call-sequence   ordered API/script calls (browsers, JS engines,",
        "#                   plugin SDKs); call-order is accepted as a synonym",
        "#   timing          timing/scheduling control (browsers, JIT)",
        "#   race            concurrent threads/processes (servers, runtimes)",
        "#   protocol-state  multi-message protocol state (TLS, HTTP servers)",
        "#   env             environment / fs state (rare: shells, init code)",
        "#   fs-state        filesystem layout (rare: archive tools)",
        "#",
        "# Note: bytes control parsed input data, not arbitrary API parameters.",
        "# If a testcase reads a parsed field and passes it as an out-of-contract",
        "# offset/size/index/lifetime parameter, report Caller contract: violated",
        "# or Parameter control: harness-only rather than treating it as security.",
        "#",
        "# The value below is a conservative seed. For a stateful library this",
        "# byte-only default under-claims — run `bin/suggest-threat-model "
        + slug + "`",
        "# for an LLM-suggested model, or edit attacker_controls by hand.",
        "[threat_model]",
    ]
    attacker_controls = (
        preserved_controls if preserved_controls is not None
        else attacker_controls_for_seed(slug, is_browser)
    )
    # toml_basic_string escapes embedded quotes/backslashes. Default tokens are
    # clean, but a preserved value carried over from a hand-edited config may
    # contain a quote — a raw f'"{v}"' would emit invalid TOML and corrupt the
    # whole file on the next placeholder refresh.
    controls_rendered = ", ".join(toml_basic_string(v) for v in attacker_controls)
    lines.append(f"attacker_controls = [{controls_rendered}]")

    # Per-sanitizer harness-link library. ubsan/msan/tsan are detected the
    # same way as asan: scan build-<san>/ for a static archive, else the
    # canonical instrumented shared object. An operator who enables a
    # second sanitizer in `enabled` and builds build-<san>/ gets a working
    # <san>_lib without a hand-edit — and export-repro then links it just
    # like asan. When the build tree is absent (the steady state — only
    # asan is built by default) or the target is a browser (links no
    # library; harnesses drive the binary), detection returns "" and the
    # field stays a commented FILL_ME placeholder.
    def _san_lib_toml_line(san: str) -> str:
        field = f"{san}_lib"
        pad = " " * (len("ubsan_lib") - len(field))
        detected = "" if is_browser else _detect_sanitizer_lib(
            root / _build_dir_name(san), root)
        if detected:
            return f"{field}{pad} = {toml_basic_string(detected)}"
        return f'# {field}{pad} = "{_build_dir_name(san)}/FILL_ME.a"'

    ubsan_lib_line = _san_lib_toml_line("ubsan")
    msan_lib_line = _san_lib_toml_line("msan")
    tsan_lib_line = _san_lib_toml_line("tsan")

    # Detect whether this target should default to findings-only mode.
    # Heuristic: a non-native ecosystem (cargo/go/python/...) where we
    # did NOT find an ASan binary in build-asan/. Cargo + go targets
    # CAN run ASan (via -Zsanitizer=address / -msan), but the operator
    # opts in by editing target.toml afterwards. Native build systems
    # (cmake/meson/autotools/mach) default to ["asan"] as they did
    # before.
    _native_build_systems = {
        "cmake", "meson", "autotools", "mach", "",
        "unknown",
    }
    # Non-native ecosystems whose default [runner] already drives sanitizer
    # builds (e.g. `swift run -sanitize={SWIFT_SANITIZER}`), so they are
    # runnable sanitizer targets rather than findings-only ones.
    _sanitizer_runner_build_systems = {"swift"}
    _findings_only_default = (
        not is_browser
        and not asan_bin
        and build_system not in _native_build_systems
        and build_system not in _sanitizer_runner_build_systems
    )

    if _findings_only_default:
        lines += [
            "",
            "# ── Sanitizer policy ─────────────────────────────────────────",
            f"# {build_system!s} target with no ASan build detected — defaulting",
            "# to FINDINGS-ONLY mode. The audit harness runs testcases through",
            "# the [runner] block below and files diagnostics under findings/",
            "# rather than crashes/. To enable a sanitizer build, uncomment",
            "# an entry in `enabled` and point the corresponding *_bin at it.",
            "# See docs/guides/multi-language.md for the per-language picture.",
            "[sanitizer]",
            "enabled = []  # findings-only — uncomment a sanitizer to enable",
            '# enabled = ["asan"]',
            f'# asan_suppressions  = "{_build_dir_name("asan")}/asan-suppressions.txt"',
            f'# ubsan_suppressions = "{_build_dir_name("ubsan")}/ubsan-suppressions.txt"',
            f'# msan_suppressions  = "{_build_dir_name("msan")}/msan-suppressions.txt"',
            f'# tsan_suppressions  = "{_build_dir_name("tsan")}/tsan-suppressions.txt"',
            f'# ubsan_bin = "{_build_dir_name("ubsan")}/{slug}"',
            f'# msan_bin  = "{_build_dir_name("msan")}/{slug}"',
            f'# tsan_bin  = "{_build_dir_name("tsan")}/{slug}"',
            ubsan_lib_line,
            msan_lib_line,
            tsan_lib_line,
            '# race      — enable for Go targets; built via `go build -race`',
        ]
    else:
        lines += [
            "",
            "# ── Sanitizer policy ─────────────────────────────────────────",
            "# enabled: which sanitizers the audit harness should run for this",
            "# target. Only ASan is on by default. Tradeoffs:",
            "#   asan   memory corruption (OOB / UAF / double-free). High signal.",
            "#   msan   reads of uninitialized memory. Recommended for self-",
            "#          contained libraries; impractical for browsers because every",
            "#          dependency (including libc++) must be MSan-instrumented.",
            "#   ubsan  undefined behaviour. Noisier — enable with a curated",
            "#          -fsanitize= check list and expect to triage false",
            "#          positives like unsigned-integer-overflow or pointer-",
            "#          overflow patterns the project considers intentional.",
            "#   tsan   data races. Highest false-positive rate in practice;",
            "#          benign atomic_relaxed and intentional racy stats counters",
            "#          will fire. Only enable with a maintained suppressions",
            "#          file for the project's threading model.",
            "#   race   Go runtime race detector. Enable for Go targets built",
            "#          with `go build -race`; emits 'WARNING: DATA RACE'.",
            "# To turn off all sanitizers and audit in findings-only mode set",
            "# `enabled = []` and configure a [runner] block below.",
            "# Per-sanitizer suppression files are appended via *_OPTIONS=",
            "# suppressions=... at runner startup. Missing files emit a warning",
            "# but do not abort.",
            "[sanitizer]",
            'enabled = ["asan"]',
            f'# asan_suppressions  = "{_build_dir_name("asan")}/asan-suppressions.txt"',
            f'# ubsan_suppressions = "{_build_dir_name("ubsan")}/ubsan-suppressions.txt"',
            f'# msan_suppressions  = "{_build_dir_name("msan")}/msan-suppressions.txt"',
            f'# tsan_suppressions  = "{_build_dir_name("tsan")}/tsan-suppressions.txt"',
            f'# ubsan_bin = "{_build_dir_name("ubsan")}/' + (slug if not is_browser else "firefox") + '"',
            f'# msan_bin  = "{_build_dir_name("msan")}/' + (slug if not is_browser else "firefox") + '"',
            f'# tsan_bin  = "{_build_dir_name("tsan")}/' + (slug if not is_browser else "firefox") + '"',
            ubsan_lib_line,
            msan_lib_line,
            tsan_lib_line,
        ]

    # ── [runner] block (language-agnostic invocation) ───────────────
    # Always emit a [runner] block (commented unless we have a default
    # for the detected build_system). bin/probe and bin/run-asan generic
    # consult it when no sanitizer binary is configured. crash_patterns
    # is read by lib/triage.sh to detect runtime crashes (panics,
    # tracebacks, race-detector reports) on top of its built-in markers.
    runner_default = language_runner_defaults(build_system)

    _toml_string = toml_basic_string  # local alias for the existing call sites

    if runner_default:
        lines += [
            "",
            "# ── Runner (language / interpreter / driver invocation) ────────",
            f"# Auto-seeded for build_system = {build_system!r}. bin/probe routes",
            "# probes here when [sanitizer].enabled = [] or no <san>_bin is set.",
            "# The token '{TESTCASE}' is substituted with the testcase path at",
            "# run time. crash_patterns are regex strings layered on top of",
            "# lib/triage.sh's built-in language-agnostic crash markers.",
            "[runner]",
            f'bin            = {_toml_string(runner_default["bin"])}',
            "args           = ["
            + ", ".join(_toml_string(a) for a in runner_default["args"])
            + "]",
        ]
        if runner_default.get("env"):
            lines.append(
                "env            = ["
                + ", ".join(_toml_string(e) for e in runner_default["env"])
                + "]"
            )
        if runner_default.get("crash_patterns"):
            patterns_str = ",\n  ".join(
                _toml_string(p) for p in runner_default["crash_patterns"]
            )
            lines.append(f"crash_patterns = [\n  {patterns_str},\n]")
    else:
        lines += [
            "",
            "# ── Runner (language / interpreter / driver invocation) ────────",
            "# Uncomment and edit if this target is a script/interpreter audit.",
            "# bin/probe routes here when [sanitizer].enabled = [] or no",
            "# <san>_bin is set. '{TESTCASE}' expands to the testcase path.",
            "# [runner]",
            '# bin            = "/usr/bin/env"',
            '# args           = ["python3", "{TESTCASE}"]',
            '# env            = []',
            '# crash_patterns = []',
        ]

    # ── S4 differential pairs (per-engine CLI flag sets) ──────────
    # Only seeded for targets with a JS shell whose flag vocabulary is
    # known. Each shell has its own JIT-toggle flags; bin/run-asan js-diff
    # reads these and falls back to Firefox flags only when missing.
    # Validate by running `<shell> --help | grep <flag>`.
    pairs = _S4_DIFF_PAIRS_TAXONOMY.get(slug, {})
    if pairs:
        lines += [
            "",
            "# ── S4 differential pairs (JIT toggle flags) ───────────────",
            "# bin/run-asan js-diff runs the testcase under both flag sets and",
            "# diffs outputs. Each engine has its own flag vocabulary. Seeded",
            "# from the per-slug taxonomy in lib/target_config.py; edit if the",
            "# engine renames flags. Verify with `<js_shell> --help | grep <flag>`.",
            "[s4_diff_pairs]",
        ]
        for key in ("jit_off", "jit_eager"):
            args = pairs.get(key, [])
            if args:
                rendered = ", ".join(f'"{a}"' for a in args)
                lines.append(f"{key:<10} = [{rendered}]")

    # Carry a curated [s6_peers] forward. A plain seed never emits this section
    # (it is added by bin/suggest-peers / hand edits), so without this a
    # placeholder refresh would silently drop the operator's peer set.
    if preserved_s6 is not None:
        domain, peers = preserved_s6
        lines += [
            "",
            "# ── S6 peers (cross-project variant mining) — preserved from prior config ──",
            "[s6_peers]",
        ]
        if domain:
            lines.append(f"domain = {toml_basic_string(domain)}")
        lines.append(
            "peers = [" + ", ".join(toml_basic_string(p) for p in peers) + "]"
        )

    out.write_text("\n".join(lines) + "\n", encoding="utf-8")


# ─── High-level Python API for python callers ─────────────────────

@dataclass
class Config:
    """Parsed + normalized target config for Python consumers.

    Field names mirror the TARGET_* shell vars callers used to read.
    """
    slug: str = ""
    target_root: str = ""
    results_dir: str = ""
    target_rev: str = ""
    session_started: str = ""
    logdir: str = ""

    upstream_url: str = ""
    asan_bin: str = ""
    asan_lib: str = ""
    ubsan_bin: str = ""
    msan_bin: str = ""
    tsan_bin: str = ""
    ubsan_lib: str = ""
    msan_lib: str = ""
    tsan_lib: str = ""
    build_system: str = ""
    cmake_target: str = ""
    pinned_rev: str = ""
    is_browser: str = "0"

    includes: list[str] = field(default_factory=list)
    link_libs: list[str] = field(default_factory=list)
    attacker_controls: list[str] = field(default_factory=list)

    # Sanitizer policy: which sanitizers this target opts into, and where to
    # find optional per-sanitizer suppression files. Defaults to ASan only
    # UNLESS the target.toml explicitly sets `enabled = []` (findings-only
    # mode, suitable for interpreted-language targets where ASan cannot run).
    sanitizers_enabled: list[str] = field(default_factory=list)
    sanitizer_suppressions: dict = field(default_factory=dict)
    sanitizer_options: dict = field(default_factory=dict)
    # True when [sanitizer].enabled was present and explicitly empty (`= []`).
    # Used to skip the legacy ["asan"] default — an interpreted-language target
    # has no ASan build to detect, and forcing ASan would block bin/probe at
    # startup. Absent section keeps the existing default behavior.
    sanitizers_explicitly_disabled: bool = False

    # S4 (differential testing) per-engine CLI flag sets. Keys mirror the
    # [s4_diff_pairs] section in target.toml — currently jit_off and
    # jit_eager. Empty when the target has no [s4_diff_pairs] section.
    s4_diff_pairs: dict[str, list[str]] = field(default_factory=dict)

    # S6 (cross-project variant mining) peer projects. Populated only from
    # [s6_peers] in target.toml. Empty when no peers are configured.
    s6_peers: list[str] = field(default_factory=list)
    s6_domain: str = ""

    # [runner] table — language-agnostic program invocation. Used by
    # bin/probe / bin/run-asan generic when no ASan binary is configured
    # (or when sanitizers are explicitly disabled). The `bin` is an
    # interpreter or driver (python3, node, go, ruby, cargo, ...). The
    # `args` list takes literal arguments; the token "{TESTCASE}" is
    # substituted with the testcase path. The `env` list is a flat list
    # of "KEY=VAL" strings layered on the runtime environment.
    # `crash_patterns` is a list of regex strings that lib/triage.sh
    # treats as additional crash signals (Go data races, Python panics,
    # any project-specific runtime markers). Empty list means
    # triage uses only its built-in language-agnostic patterns.
    runner_bin: str = ""
    runner_args: list[str] = field(default_factory=list)
    runner_env: list[str] = field(default_factory=list)
    runner_crash_patterns: list[str] = field(default_factory=list)

    def attacker_controls_csv(self) -> str:
        if not self.attacker_controls:
            return "bytes"
        seen: set[str] = set()
        out: list[str] = []
        for v in self.attacker_controls:
            if v in seen:
                continue
            seen.add(v)
            out.append(v)
        return ",".join(out)

    def sanitizers_enabled_csv(self) -> str:
        if not self.sanitizers_enabled:
            # Explicitly disabled: return empty CSV. Callers distinguish
            # "no sanitizer" from "default asan" via the empty string.
            return "" if self.sanitizers_explicitly_disabled else "asan"
        seen: set[str] = set()
        out: list[str] = []
        for v in self.sanitizers_enabled:
            if v in seen:
                continue
            seen.add(v)
            out.append(v)
        return ",".join(out)

    def sanitizer_is_enabled(self, name: str) -> bool:
        return name.lower() in self.sanitizers_enabled

    def sanitizer_suppressions_path(self, name: str) -> str:
        """Absolute path to the suppression file for `name`, or "" if unset.

        Caller should check existence — missing files emit a warning at
        runner startup but do not abort, so audits can proceed before a
        per-target suppression list has been authored.
        """
        p = self.sanitizer_suppressions.get(name.lower(), "")
        if not p:
            return ""
        return self.resolve_path(p)

    def sanitizer_bin(self, name: str) -> str:
        name = name.lower()
        if name == "asan":
            return self.asan_bin
        if name == "ubsan":
            return self.ubsan_bin
        if name == "msan":
            return self.msan_bin
        if name == "tsan":
            return self.tsan_bin
        return ""

    def sanitizer_lib(self, name: str) -> str:
        name = name.lower()
        if name == "asan":
            return self.asan_lib
        if name == "ubsan":
            return self.ubsan_lib
        if name == "msan":
            return self.msan_lib
        if name == "tsan":
            return self.tsan_lib
        return ""

    def resolve_path(self, p: str) -> str:
        if not p:
            return ""
        if p.startswith("/"):
            return p
        norm = os.path.normpath(p)
        if norm == ".." or norm.startswith("../"):
            raise ValueError(f"target-relative path escapes TARGET_ROOT: {p}")
        suffix = os.environ.get("AUDIT_BUILD_SUFFIX", "")
        if suffix:
            head, sep, rest = norm.partition("/")
            if head in ("build-asan", "build-ubsan", "build-msan", "build-tsan"):
                norm = f"{head}{suffix}{sep}{rest}"
        return f"{self.target_root}/{norm}"


def _apply_attacker_controls(cfg: Config, raw: list, source_path: str) -> None:
    for item in raw:
        if not isinstance(item, str) or not item:
            continue
        norm = _normalize_attacker_control(item)
        if _is_valid_attacker_control(norm):
            cfg.attacker_controls.append(norm)
        else:
            valid = " ".join(ATTACKER_CONTROLS_VALID)
            sys.stderr.write(
                f"[target] ignored unknown attacker_controls token in {source_path}: "
                f"'{item}' (valid: {valid})\n"
            )
    if not cfg.attacker_controls:
        cfg.attacker_controls.append("bytes")


def _apply_sanitizer_section(cfg: Config, raw: dict, source_path: str) -> None:
    """Populate sanitizer policy from a [sanitizer] subtable.

    Accepts:
      enabled = ["asan", ...]              # list of sanitizer slugs
      <name>_suppressions = "path"          # path to suppression file
      <name>_options     = "key=val:..."   # extra runtime opts to append
      <name>_bin         = "path"           # per-sanitizer binary override
                                            # (asan_bin still lives top-level)
      <name>_lib         = "path"           # per-sanitizer C/C++ harness lib
                                            # (asan_lib still lives top-level)

    Unknown sanitizer slugs in `enabled` get a stderr warning and are
    dropped. If `enabled` is present and explicitly empty (`= []`), the
    loader honors it as findings-only mode — used by interpreted-language
    targets without a sanitizer build. Absent `enabled` keeps the legacy
    fallback to ["asan"].
    """
    # Distinguish "key absent" from "key present but empty". Real TOML
    # parsing returns the actual list; the lenient fallback parser also
    # accepts an empty `[]`. Using a sentinel default avoids confusing
    # them with each other.
    _SENTINEL: object = object()
    raw_enabled = raw.get("enabled", _SENTINEL)
    if raw_enabled is _SENTINEL:
        # Section present but `enabled` key absent → keep legacy default
        # (caller will append "asan" if cfg.sanitizers_enabled ends empty).
        pass
    elif isinstance(raw_enabled, list):
        if not raw_enabled:
            cfg.sanitizers_explicitly_disabled = True
        for item in raw_enabled:
            if not isinstance(item, str) or not item:
                continue
            norm = item.lower().strip()
            if norm in SANITIZERS_VALID:
                if norm not in cfg.sanitizers_enabled:
                    cfg.sanitizers_enabled.append(norm)
            else:
                valid = " ".join(SANITIZERS_VALID)
                sys.stderr.write(
                    f"[target] ignored unknown sanitizer in {source_path}: "
                    f"'{item}' (valid: {valid})\n"
                )
    for san in SANITIZERS_VALID:
        sup_key = f"{san}_suppressions"
        if isinstance(raw.get(sup_key), str) and raw[sup_key]:
            cfg.sanitizer_suppressions[san] = raw[sup_key]
        opt_key = f"{san}_options"
        if isinstance(raw.get(opt_key), str) and raw[opt_key]:
            cfg.sanitizer_options[san] = raw[opt_key]
        bin_key = f"{san}_bin"
        if isinstance(raw.get(bin_key), str) and raw[bin_key]:
            # asan_bin stays at top level for back-compat; only fill the
            # ubsan/msan/tsan/race overrides from this section. (Go's race
            # detector does not have a separate Config field — it is
            # detected from any binary's output by the runtime crash signal.)
            if san == "ubsan":
                cfg.ubsan_bin = raw[bin_key]
            elif san == "msan":
                cfg.msan_bin = raw[bin_key]
            elif san == "tsan":
                cfg.tsan_bin = raw[bin_key]
        lib_key = f"{san}_lib"
        if isinstance(raw.get(lib_key), str) and raw[lib_key]:
            # asan_lib stays at top level for back-compat; non-ASan
            # libraries live under [sanitizer] so one probe run cannot
            # accidentally link an ASan archive into an MSan/TSan/UBSan build.
            if san == "ubsan":
                cfg.ubsan_lib = raw[lib_key]
            elif san == "msan":
                cfg.msan_lib = raw[lib_key]
            elif san == "tsan":
                cfg.tsan_lib = raw[lib_key]


def _apply_runner_section(cfg: Config, raw: dict, source_path: str) -> None:
    """Populate cfg.runner_* from a [runner] subtable.

    Accepts:
      bin            = "/usr/bin/python3"       # interpreter or driver
      args           = ["-X", "dev", "{TESTCASE}"]  # literal args (use
                                                    # "{TESTCASE}" token
                                                    # for the testcase path)
      env            = ["GORACE=halt_on_error=1"]  # env vars layered on
      crash_patterns = ["my project's crash header"]  # extra regex signals

    Unknown keys are ignored. The loader keeps all the literal strings —
    bin/probe and lib/triage.sh substitute "{TESTCASE}" at run time.
    """
    if isinstance(raw.get("bin"), str) and raw["bin"]:
        cfg.runner_bin = raw["bin"]
    if isinstance(raw.get("args"), list):
        cfg.runner_args = [x for x in raw["args"] if isinstance(x, str)]
    if isinstance(raw.get("env"), list):
        cfg.runner_env = [x for x in raw["env"] if isinstance(x, str) and "=" in x]
    if isinstance(raw.get("crash_patterns"), list):
        cfg.runner_crash_patterns = [
            x for x in raw["crash_patterns"] if isinstance(x, str) and x
        ]


def load_toml_into(cfg: Config, toml_path: str | os.PathLike) -> None:
    """Parse a target.toml and populate the Config in-place.

    Mirrors target_load_toml: handles the [threat_model] section,
    normalizes call-order → call-sequence, drops unknown tokens with a
    stderr warning, defaults attacker_controls to ["bytes"] when empty.
    """
    parsed = parse_toml(toml_path)
    p = str(toml_path)
    cfg.attacker_controls = []
    cfg.includes = []
    cfg.link_libs = []
    cfg.sanitizers_enabled = []
    cfg.sanitizer_suppressions = {}
    cfg.sanitizer_options = {}
    cfg.sanitizers_explicitly_disabled = False
    cfg.s4_diff_pairs = {}
    cfg.s6_peers = []
    cfg.s6_domain = ""
    cfg.ubsan_bin = ""
    cfg.msan_bin = ""
    cfg.tsan_bin = ""
    cfg.ubsan_lib = ""
    cfg.msan_lib = ""
    cfg.tsan_lib = ""
    cfg.runner_bin = ""
    cfg.runner_args = []
    cfg.runner_env = []
    cfg.runner_crash_patterns = []
    for k, v in parsed.items():
        if k in ("target", "slug") and isinstance(v, str):
            cfg.slug = cfg.slug or v
        elif k == "upstream_url" and isinstance(v, str):
            cfg.upstream_url = v
        elif k == "asan_bin" and isinstance(v, str):
            cfg.asan_bin = v
        elif k == "asan_lib" and isinstance(v, str):
            cfg.asan_lib = v
        elif k == "cmake_target" and isinstance(v, str):
            cfg.cmake_target = v
        elif k == "build_system" and isinstance(v, str):
            cfg.build_system = v
        elif k == "pinned_rev" and isinstance(v, str):
            cfg.pinned_rev = v
        elif k == "is_browser":
            cfg.is_browser = _toml_value_to_str(v)
        elif k == "includes" and isinstance(v, list):
            cfg.includes = [x for x in v if isinstance(x, str) and x]
        elif k == "link_libs" and isinstance(v, list):
            cfg.link_libs = [x for x in v if isinstance(x, str) and x]
        elif k == "threat_model" and isinstance(v, dict):
            ac = v.get("attacker_controls", [])
            if isinstance(ac, list):
                _apply_attacker_controls(cfg, ac, p)
        elif k == "sanitizer" and isinstance(v, dict):
            _apply_sanitizer_section(cfg, v, p)
        elif k == "runner" and isinstance(v, dict):
            _apply_runner_section(cfg, v, p)
        elif k == "s4_diff_pairs" and isinstance(v, dict):
            for pair_key, pair_val in v.items():
                if isinstance(pair_val, list):
                    cfg.s4_diff_pairs[str(pair_key)] = [
                        x for x in pair_val if isinstance(x, str) and x
                    ]
        elif k == "s6_peers" and isinstance(v, dict):
            peers = v.get("peers", [])
            if isinstance(peers, list):
                cfg.s6_peers = [x for x in peers if isinstance(x, str) and x]
            dom = v.get("domain", "")
            if isinstance(dom, str):
                cfg.s6_domain = dom
    if not cfg.attacker_controls:
        cfg.attacker_controls.append("bytes")
    # Default: ASan only — UNLESS the target.toml explicitly set
    # `[sanitizer].enabled = []`, which means findings-only mode for
    # interpreted-language / sanitizer-less targets. The
    # sanitizers_explicitly_disabled flag preserves that distinction
    # across legacy callers that look at sanitizers_enabled directly.
    if not cfg.sanitizers_enabled and not cfg.sanitizers_explicitly_disabled:
        cfg.sanitizers_enabled.append("asan")

    # Normalize any path field that was stored as an audit-machine absolute
    # path (e.g. `auto-repair-target-toml` proposed
    # `/Users/.../targets/<slug>/build-asan/lib/libfoo.a`). Downstream
    # consumers (bin/export-repro _strip_sanitizer_build_prefix, the
    # seeder's `removeprefix`) assume target_root-relative form. Leaving
    # an absolute path in cfg causes reproduce.sh to emit
    # `$build/targets/<slug>/build-asan/...` instead of
    # `$build/build-asan/...`, breaking the maintainer build.
    _strip_target_root_paths(cfg)


def _strip_target_root_paths(cfg: Config) -> None:
    """Rewrite absolute target_root-prefixed values to target_root-relative."""
    root = (cfg.target_root or "").rstrip("/")
    if not root:
        return
    prefix = root + "/"

    def _rel(value: str) -> str:
        return value[len(prefix):] if value.startswith(prefix) else value

    cfg.asan_bin = _rel(cfg.asan_bin)
    cfg.asan_lib = _rel(cfg.asan_lib)
    cfg.ubsan_bin = _rel(cfg.ubsan_bin)
    cfg.ubsan_lib = _rel(cfg.ubsan_lib)
    cfg.msan_bin = _rel(cfg.msan_bin)
    cfg.msan_lib = _rel(cfg.msan_lib)
    cfg.tsan_bin = _rel(cfg.tsan_bin)
    cfg.tsan_lib = _rel(cfg.tsan_lib)
    # link_libs may carry bare archives or source compile inputs that
    # auto-repair-target-toml dragged in. Don't touch `-l…` / `-L…` /
    # `-Wl,…` flag entries.
    cfg.link_libs = [
        _rel(item) if not item.startswith("-") else item
        for item in cfg.link_libs
    ]


def load(start: str | os.PathLike) -> Config:
    """One-shot: discover slug dir, read .session-env, parse target.toml."""
    slug_dir = find_session_dir(start)
    if slug_dir is None:
        raise FileNotFoundError(
            f"no output/<slug>/.session-env found above: {start} "
            f"(run bin/audit first, or pass --target)"
        )
    cfg = Config()
    env = read_session_env(slug_dir)
    cfg.results_dir = env.get("RESULTS_DIR", "")
    cfg.target_root = env.get("TARGET_ROOT", "")
    cfg.slug = env.get("TARGET_SLUG", "")
    cfg.target_rev = env.get("TARGET_REV", "")
    cfg.session_started = env.get("SESSION_STARTED", "")
    cfg.logdir = env.get("LOGDIR", "")
    toml_path = target_toml_for_session_dir(slug_dir)
    if not toml_path.is_file():
        raise FileNotFoundError(f"target.toml not found: {toml_path}")
    load_toml_into(cfg, toml_path)
    return cfg


