NO_REV = "norev"

# Revision / URL sentinels meaning "no usable upstream was recorded" — a
# local-only target, or a checkout with no VCS, or an un-seeded target.toml.
# Single source of truth for the report and link builders (bin/export-repro,
# lib/report_enrich) so they agree on what is worth a "clones upstream@<rev>"
# hint or a /blob/<rev>/ source link. Mirrors lib/benchmark._hash_text.
#
# "HEAD" is deliberately NOT unpinned: it clones and resolves to a forge's
# default branch, so a target with a real upstream but no recorded commit
# still gets a working hint/link. The only cost is a non-permalink line ref,
# which is strictly more useful to a maintainer than no link at all.
_UNPINNED_REVS = frozenset({"", NO_REV, "no-vcs", "unknown", "?"})
_PLACEHOLDER_URLS = frozenset({"", "fill_me"})


def is_unpinned_rev(rev: str) -> bool:
    """True when `rev` is a sentinel meaning 'no usable upstream commit'.

    `HEAD` is treated as usable (returns False) — see `_UNPINNED_REVS`."""
    return (rev or "").strip().lower() in _UNPINNED_REVS


def is_placeholder_url(url: str) -> bool:
    """True when `url` is empty or the un-seeded target.toml `FILL_ME` stub."""
    return (url or "").strip().lower() in _PLACEHOLDER_URLS


def config_has_live_placeholder(toml_path: "str | os.PathLike") -> bool:
    """True when target.toml has an UNRESOLVED placeholder a re-seed can fix —
    an active (parsed, non-comment) field whose value still contains FILL_ME.

    bin/setup-target uses this to decide whether to refresh a generated config.
    Two things are deliberately NOT treated as placeholders, so a reviewed
    config keeps the operator's edits across reruns instead of being rewritten:

      * commented example lines such as `# ubsan_lib = "build-<san>/FILL_ME.a"`,
        which the seed always emits and the TOML parser never sees; and
      * `upstream_url = "FILL_ME"` on a local-only target (unpinned pinned_rev),
        which is the intended steady state — re-seeding cannot resolve it.

    A grep for FILL_ME cannot make these distinctions (it matches comments and
    the local-only upstream), which is why this parses the TOML instead.
    """
    try:
        parsed = parse_toml(Path(toml_path))
    except Exception:
        return False  # unparsable config is handled by the caller's own check
    upstream_unresolvable = is_unpinned_rev(str(parsed.get("pinned_rev", "") or ""))

    def _walk(obj, key=None) -> bool:
        if isinstance(obj, str):
            if "FILL_ME" not in obj:
                return False
            return not (key == "upstream_url" and upstream_unresolvable)
        if isinstance(obj, dict):
            return any(_walk(v, k) for k, v in obj.items())
        if isinstance(obj, list):
            return any(_walk(v, key) for v in obj)
        return False

    return _walk(parsed)

# TOML basic-string escaping. Used everywhere we write a Python string
# into a generated target.toml so an unsanitised value (a tainted slug,
# an LLM-suggested peer name with an embedded quote, a Windows-style
# regex with backslashes) cannot break the file. Mirrors the TOML 1.0
# spec's escape table; anything outside basic ASCII / above 0x7E is
# emitted as a \uXXXX sequence so editors render the output sanely.
_TOML_BASIC_ESCAPES = {
    "\\": "\\\\",
    '"':  '\\"',
    "\b": "\\b",
    "\t": "\\t",
    "\n": "\\n",
    "\f": "\\f",
    "\r": "\\r",
}


def toml_basic_string(value: str) -> str:
    """Render `value` as a TOML basic-string literal (including the quotes).

    Escapes backslash, double-quote, the control-character set TOML calls
    out by name, and anything else below 0x20 / equal to 0x7F as a \\uXXXX
    sequence. Non-strings are stringified first so callers can pass through
    arbitrary scalar metadata without a TypeError.
    """
    if not isinstance(value, str):
        value = "" if value is None else str(value)
    out: list[str] = ['"']
    for ch in value:
        esc = _TOML_BASIC_ESCAPES.get(ch)
        if esc is not None:
            out.append(esc)
            continue
        code = ord(ch)
        if code < 0x20 or code == 0x7F:
            out.append(f"\\u{code:04X}")
        else:
            out.append(ch)
    out.append('"')
    return "".join(out)


def toml_comment_lines(text: str) -> str:
    """Render arbitrary text as TOML comment(s), one line per source line.

    Every line (split on \\n / \\r\\n / \\r) is prefixed with "# " so a
    multi-line LLM reasoning blob cannot escape the comment context and
    turn second-line content into active TOML. Empty input collapses to
    a single bare "#" so the surrounding layout is preserved.
    """
    if text is None:
        return "#"
    s = str(text)
    if not s:
        return "#"
    lines = s.splitlines() or [""]
    return "\n".join("# " + line for line in lines)


# Canonical attacker_controls tokens. Anything outside this set is logged on
# stderr and dropped (silent acceptance would weaken lib/triage.sh's verdict
# matrix). Order matches lib/target_config.sh:TARGET_ATTACKER_CONTROLS_VALID.
ATTACKER_CONTROLS_VALID = (
    "bytes", "call-sequence", "call-order", "timing",
    "race", "env", "protocol-state", "fs-state",
)

# Canonical sanitizer slugs. Only ASan is enabled by default; the rest must be
# opted in per target via [sanitizer].enabled because MSan needs the whole
# dependency tree instrumented, and UBSan/TSan benefit from triage-aware
# suppression files. Go's race detector ("race") is wired in for Go targets —
# it emits "WARNING: DATA RACE" reports that lib/triage.sh treats as
# diagnostic signal. Anything outside this set is logged on stderr and dropped.
SANITIZERS_VALID = ("asan", "ubsan", "msan", "tsan", "race")

# .session-env keys we trust to import into the parent shell environment.
SESSION_ENV_ALLOW = (
    "RESULTS_DIR", "TARGET_ROOT", "TARGET_SLUG",
    "TARGET_REV", "TARGET_REPO_TYPE", "SESSION_STARTED", "LOGDIR",
)

# Section/key names emitted as shell variables must restrict to [A-Za-z0-9_]
# so they're safe to splice into TARGET_<SECTION>_<KEY>. The legacy parser
# used this to discard invalid section headers; default parsing is now strict
# and only uses the legacy behavior when TARGET_TOML_LENIENT=1.
_SHELL_NAME_RE = re.compile(r"^[A-Za-z0-9_]+$")


def _normalize_attacker_control(v: str) -> str:
    v = v.lower()
    if v == "call-order":
        v = "call-sequence"
    return v


def _is_valid_attacker_control(v: str) -> bool:
    return v in ATTACKER_CONTROLS_VALID


def _strip_bad_section_headers(text: str) -> str:
    """Drop `[<name>]` headers whose name has chars outside [A-Za-z0-9_].

    Mirrors the bash parser, which silently absorbed bad sections and let
    the keys after them fall back to top-level. Real TOML would error on
    `[bad section name with spaces]`; this preserves the lenient behavior.
    """
    out_lines = []
    section_re = re.compile(r"^\s*\[([^\]]*)\]\s*(?:#.*)?$")
    for line in text.splitlines(keepends=True):
        m = section_re.match(line.rstrip("\n"))
        if m:
            name = m.group(1).strip()
            if not name or not _SHELL_NAME_RE.match(name):
                # Drop the header — keys that follow go to the previous
                # (or top-level) scope. Replace with blank to keep line
                # numbers stable for any future error messages.
                out_lines.append("\n")
                continue
        out_lines.append(line)
    return "".join(out_lines)


def _toml_value_to_str(v) -> str:
    """Render a scalar TOML value the way the bash parser produced it.

    Strings come through as-is. Bools/ints/floats render as their Python
    str() form. The bash parser only emitted strings, so any non-string
    scalar in target.toml is a behavior change (loud, not silent).
    """
    if isinstance(v, str):
        return v
    if isinstance(v, bool):
        return "1" if v else "0"
    return str(v)


class _FallbackTOMLDecodeError(ValueError):
    pass


def _strip_inline_comment(line: str) -> str:
    in_quote = False
    escaped = False
    for i, ch in enumerate(line):
        if escaped:
            escaped = False
            continue
        if ch == "\\" and in_quote:
            escaped = True
            continue
        if ch == '"':
            in_quote = not in_quote
            continue
        if ch == "#" and not in_quote:
            return line[:i]
    return line


def _split_array_items(body: str) -> list[str]:
    items: list[str] = []
    start = 0
    in_quote = False
    escaped = False
    for i, ch in enumerate(body):
        if escaped:
            escaped = False
            continue
        if ch == "\\" and in_quote:
            escaped = True
            continue
        if ch == '"':
            in_quote = not in_quote
            continue
        if ch == "," and not in_quote:
            items.append(body[start:i].strip())
            start = i + 1
    if in_quote:
        raise _FallbackTOMLDecodeError("unterminated quoted array item")
    items.append(body[start:].strip())
    return items


def _array_literal_is_closed(value: str) -> bool:
    in_quote = False
    escaped = False
    for ch in value:
        if escaped:
            escaped = False
            continue
        if ch == "\\" and in_quote:
            escaped = True
            continue
        if ch == '"':
            in_quote = not in_quote
            continue
        if ch == "]" and not in_quote:
            return True
    return False


def _parse_basic_string(value: str) -> str:
    out: list[str] = []
    i = 0
    escapes = {
        "b": "\b",
        "t": "\t",
        "n": "\n",
        "f": "\f",
        "r": "\r",
        '"': '"',
        "\\": "\\",
    }
    while i < len(value):
        ch = value[i]
        if ch != "\\":
            out.append(ch)
            i += 1
            continue
        i += 1
        if i >= len(value):
            raise _FallbackTOMLDecodeError("unterminated string escape")
        esc = value[i]
        if esc in escapes:
            out.append(escapes[esc])
            i += 1
            continue
        if esc in ("u", "U"):
            n = 4 if esc == "u" else 8
            code = value[i + 1:i + 1 + n]
            if len(code) != n or not re.match(r"^[0-9A-Fa-f]+$", code):
                raise _FallbackTOMLDecodeError(f"invalid unicode escape: \\{esc}{code}")
            out.append(chr(int(code, 16)))
            i += 1 + n
            continue
        raise _FallbackTOMLDecodeError(f"unsupported string escape: \\{esc}")
    return "".join(out)


def _parse_array_value(val: str) -> list[str]:
    if not (val.startswith("[") and val.endswith("]")):
        raise _FallbackTOMLDecodeError(f"unsupported TOML array: {val}")
    body = val[1:-1].strip()
    if body == "":
        return []
    items = []
    for part in _split_array_items(body):
        item = part.strip()
        if item == "":
            continue
        if len(item) >= 2 and item[0] == item[-1] == '"':
            items.append(_parse_basic_string(item[1:-1]))
        else:
            raise _FallbackTOMLDecodeError(f"unsupported TOML array item: {part}")
    return items


def _parse_simple_toml(text: str) -> dict:
    parsed: dict = {}
    current = parsed
    section_re = re.compile(r"^\s*\[([A-Za-z0-9_-]+)\]\s*$")
    scalar_re = re.compile(r'^\s*([A-Za-z0-9_]+)\s*=\s*(.*?)\s*$')

    raw_lines = text.splitlines()
    i = 0
    while i < len(raw_lines):
        raw_line = raw_lines[i]
        i += 1
        line = _strip_inline_comment(raw_line).strip()
        if not line:
            continue
        sec = section_re.match(line)
        if sec:
            current = parsed.setdefault(sec.group(1), {})
            continue
        m = scalar_re.match(line)
        if not m:
            raise _FallbackTOMLDecodeError(f"unsupported TOML line: {raw_line}")
        key, val = m.groups()
        val = val.strip()
        if val.startswith("["):
            while not _array_literal_is_closed(val):
                if i >= len(raw_lines):
                    raise _FallbackTOMLDecodeError(f"unterminated TOML array for key: {key}")
                val += "\n" + _strip_inline_comment(raw_lines[i]).strip()
                i += 1
            current[key] = _parse_array_value(val)
        elif len(val) >= 2 and val[0] == val[-1] == '"':
            current[key] = _parse_basic_string(val[1:-1])
        elif val.lower() in ("true", "false"):
            current[key] = val.lower() == "true"
        else:
            current[key] = val
    return parsed


def parse_toml(path: str | os.PathLike) -> dict:
    """Parse a target.toml.

    Strict TOML parsing is the default. Set TARGET_TOML_LENIENT=1 only for
    migration of legacy local configs that relied on the old bash parser's
    behavior of ignoring invalid section headers.
    """
    with open(path, "rb") as f:
        raw = f.read()
    text = raw.decode("utf-8", errors="replace")
    if os.environ.get("TARGET_TOML_LENIENT") == "1":
        pre = _strip_bad_section_headers(text)
    else:
        pre = text
    if tomllib is not None:
        return tomllib.loads(pre)
    return _parse_simple_toml(pre)


def _emit_legacy_stream(parsed: dict) -> bytes:
    """Emit the NUL-delimited KEY\\0VALUE\\0... stream consumed by the
    bash target_load_toml loop.

    Output keys match the bash parser exactly:
      top-level scalar foo  -> TARGET_FOO\\0value\\0
      top-level array  foo  -> TARGET_FOO_LIST\\0joined\\0  (entries \\n-terminated)
      [section] scalar bar  -> TARGET_<SECTION>_BAR\\0value\\0
      [section] array  bar  -> TARGET_<SECTION>_BAR_LIST\\0joined\\0
    """
    out = bytearray()

    def emit_scalar(key: str, value: str) -> None:
        out.extend(key.encode("utf-8"))
        out.append(0)
        out.extend(value.encode("utf-8"))
        out.append(0)

    def emit_list(key: str, items: list) -> None:
        joined = ""
        for item in items:
            if isinstance(item, str):
                joined += item + "\n"
            else:
                joined += _toml_value_to_str(item) + "\n"
        out.extend(key.encode("utf-8"))
        out.append(0)
        out.extend(joined.encode("utf-8"))
        out.append(0)

    def visit(d: dict, prefix: str) -> None:
        for k, v in d.items():
            if not _SHELL_NAME_RE.match(k):
                # Section or key with chars unsafe for shell-var splicing
                # — preserve bash behavior of silently dropping it.
                continue
            shell_key = f"{prefix}{k.upper()}"
            if isinstance(v, dict):
                # Single nested section per key, e.g. [threat_model].
                # Bash parser flattens to TARGET_<SECTION>_<KEY>.
                visit(v, f"{shell_key}_")
            elif isinstance(v, list):
                emit_list(f"{shell_key}_LIST", v)
            else:
                emit_scalar(shell_key, _toml_value_to_str(v))

    visit(parsed, "TARGET_")
    return bytes(out)


# ─── Discovery ──────────────────────────────────────────────────────

def find_session_dir(start: str | os.PathLike) -> Optional[Path]:
    """Walk up from `start` looking for the nearest .session-env.

    Testcases live under output/<slug>/<backend>/results/scratch-N, so the
    nearest backend-local results/.session-env wins and avoids cross-backend
    races. For legacy/manual callers outside a results tree, also scan
    output/<slug>/.session-env and output/<slug>/<backend>/results/.session-env
    below each ancestor.
    """
    p = Path(start)
    if not p.exists():
        p = Path.cwd()
    if not p.is_dir():
        p = p.parent
    try:
        cur = p.resolve()
    except OSError:
        return None
    def scan_output_tree(base: Path) -> Optional[Path]:
        out_dir = base / "output"
        if not out_dir.is_dir():
            return None
        try:
            targets = sorted(out_dir.iterdir())
        except OSError:
            return None
        for target in targets:
            if not target.is_dir():
                continue
            # Backend-local output/<slug>/<backend>/results/.session-env is
            # authoritative; prefer it over the legacy output/<slug>/
            # .session-env copy, which concurrent backends race on.
            try:
                backends = sorted(target.iterdir())
            except OSError:
                backends = []
            for backend in backends:
                cand = backend / "results"
                if cand.is_dir() and (cand / ".session-env").is_file():
                    return cand
            if (target / ".session-env").is_file():
                return target
        return None

    while True:
        if (cur / ".session-env").is_file():
            return cur
        found = scan_output_tree(cur)
        if found is not None:
            return found
        if cur.parent == cur:
            return None
        cur = cur.parent


def target_toml_for_session_dir(session_dir: str | os.PathLike) -> Path:
    """Return target.toml for a legacy slug dir or backend results dir."""
    d = Path(session_dir)
    # output/<slug>/<backend>/results/.session-env -> output/<slug>/target.toml
    if d.name == "results":
        return d.parent.parent / "target.toml"
    return d / "target.toml"


def find_slug_session_dir(slug_dir: str | os.PathLike) -> Optional[Path]:
    """Resolve the session dir for a known output/<slug> directory.

    `find_session_dir` walks *up* from an arbitrary path; this resolves
    the other direction — caller already knows the slug dir and wants its
    .session-env. A backend-local output/<slug>/<backend>/results/
    .session-env is authoritative and wins over the legacy
    output/<slug>/.session-env copy (which concurrent backends race on).
    With several backends present the lexicographically-first is returned
    so the choice is deterministic. Returns None when neither exists.
    """
    d = Path(slug_dir)
    if d.is_dir():
        for backend in sorted(d.iterdir()):
            if not backend.is_dir():
                continue
            cand = backend / "results"
            if (cand / ".session-env").is_file():
                return cand
    if (d / ".session-env").is_file():
        return d
    return None


# ─── .session-env ──────────────────────────────────────────────────

def read_session_env(slug_dir: str | os.PathLike) -> dict[str, str]:
    """Read .session-env, returning the allowlisted KEY -> value map."""
    env_file = Path(slug_dir) / ".session-env"
    if not env_file.is_file():
        raise FileNotFoundError(f"session-env not found: {env_file}")
    out: dict[str, str] = {}
    with open(env_file, "r", encoding="utf-8", errors="replace") as f:
        for line in f:
            line = line.rstrip("\n")
            if not line or line.startswith("#"):
                continue
            key, _, value = line.partition("=")
            if key in SESSION_ENV_ALLOW:
                out[key] = value
    return out


def write_session_env(
    slug_dir: str | os.PathLike,
    results: str,
    target_root: str,
    slug: str,
    rev: str,
    logdir: str,
) -> None:
    """Write a fresh .session-env (matches target_write_session_env)."""
    d = Path(slug_dir)
    d.mkdir(parents=True, exist_ok=True)
    started = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    rev_v = rev or "unknown"
    # Persist the VCS type so downstream tools (e.g. bin/reachability) get the
    # authoritative decision from resumable state without re-resolving the
    # target root from output-path heuristics. Detected from the same
    # target_root the audit binds, so it cannot disagree with bin/audit.
    try:
        repo_type = detect_repo_type(target_root)
    except Exception:
        repo_type = "none"
    lines = [
        "# Written by bin/audit at session start. Sourced by bin/probe and friends.",
        f"RESULTS_DIR={results}",
        f"TARGET_ROOT={target_root}",
        f"TARGET_SLUG={slug}",
        f"TARGET_REV={rev_v}",
        f"TARGET_REPO_TYPE={repo_type}",
        f"LOGDIR={logdir}",
        f"SESSION_STARTED={started}",
    ]
    tmp_name = ""
    env_path = d / ".session-env"
    try:
        with tempfile.NamedTemporaryFile(
            "w",
            encoding="utf-8",
            dir=d,
            prefix=".session-env.",
            suffix=".tmp",
            delete=False,
        ) as f:
            tmp_name = f.name
            f.write("\n".join(lines) + "\n")
            f.flush()
            os.fsync(f.fileno())
        os.replace(tmp_name, env_path)
        # Best-effort directory fsync so the rename is durable. Both the
        # open and the fsync can fail on filesystems that do not support
        # syncing a directory fd (some networked / sandboxed mounts) — the
        # rename itself already landed, so a failure here must not abort
        # the caller (this runs at audit session start).
        try:
            fd = os.open(str(d), os.O_RDONLY)
        except OSError:
            return
        try:
            os.fsync(fd)
        except OSError:
            pass
        finally:
            os.close(fd)
    finally:
        if tmp_name:
            try:
                os.unlink(tmp_name)
            except FileNotFoundError:
                pass


# ─── git/hg/plain source detection ──────────────────────────────────

def detect_repo_type(target_root: str | os.PathLike) -> str:
    """Return git, hg, or none for a target checkout.

    A target nested under the harness repo is still a plain target unless it
    has direct VCS metadata. That avoids treating targets/foo as a git
    checkout only because /work/.git exists above it.
    """
    root = Path(target_root)
    if (root / ".hg").is_dir():
        return "hg"
    if not (root / ".git").exists():
        return "none"
    try:
        out = subprocess.run(
            ["git", "-C", str(root), "rev-parse", "--git-dir"],
            capture_output=True, text=True, timeout=10,
        )
        if out.returncode == 0:
            return "git"
    except (OSError, subprocess.TimeoutExpired):
        pass
    return "none"


def detect_rev(target_root: str | os.PathLike) -> str:
    root = Path(target_root)
    repo_type = detect_repo_type(root)
    if repo_type == "git":
        try:
            out = subprocess.run(
                ["git", "-C", str(root), "rev-parse", "HEAD"],
                capture_output=True, text=True, timeout=10,
            )
            if out.returncode == 0:
                return out.stdout.strip()
        except (OSError, subprocess.TimeoutExpired):
            pass
        return ""

    if repo_type == "hg":
        try:
            out = subprocess.run(
                ["hg", "--cwd", str(root), "id", "-i"],
                capture_output=True, text=True, timeout=10,
            )
            if out.returncode == 0:
                return out.stdout.strip().rstrip("+")
        except (OSError, subprocess.TimeoutExpired):
            pass
        return ""

    if (root / ".git").exists():
        return ""

    if root.is_dir():
        return NO_REV
    return ""


