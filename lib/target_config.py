_BROWSER_BIN_CANDIDATES = (
    "dist/Nightly.app/Contents/MacOS/firefox",
    "dist/bin/firefox",
)

# CMake `install(TARGETS ...)` keywords that terminate the target-name list.
# Anything after one of these is install configuration, not a target name.
_CMAKE_INSTALL_KEYWORDS = frozenset({
    "EXPORT", "ARCHIVE", "LIBRARY", "RUNTIME", "OBJECTS", "FRAMEWORK", "BUNDLE",
    "PRIVATE_HEADER", "PUBLIC_HEADER", "RESOURCE", "INCLUDES", "DESTINATION",
    "PERMISSIONS", "CONFIGURATIONS", "COMPONENT", "NAMELINK_COMPONENT",
    "NAMELINK_ONLY", "NAMELINK_SKIP", "OPTIONAL", "EXCLUDE_FROM_ALL",
    "FILE_SET", "TYPE", "CXX_MODULES_BMI",
})


def _manifest_texts(target_root: Path, filename: str, max_depth: int = 3) -> list[str]:
    """Return the text of every `filename` within `max_depth` of target_root.

    Bounded walk: prunes sanitizer build trees, VCS dirs and dependency
    caches so a seed-time scan never descends an entire source checkout.
    """
    texts: list[str] = []
    root_str = str(target_root)
    for dirpath, dirnames, filenames in os.walk(target_root):
        depth = dirpath[len(root_str):].count(os.sep)
        if depth >= max_depth:
            dirnames[:] = []
        dirnames[:] = [
            d for d in dirnames
            if not d.startswith(("build-", ".")) and d != "node_modules"
        ]
        if filename in filenames:
            try:
                texts.append(
                    Path(dirpath, filename).read_text(
                        encoding="utf-8", errors="replace")
                )
            except OSError:
                pass
    return texts


def _autotools_installed_programs(texts: Iterable[str]) -> list[str]:
    """Names from `bin_PROGRAMS` / `sbin_PROGRAMS` in automake Makefile.am text.

    Only the *installed* program primaries are read — `check_PROGRAMS` and
    `noinst_PROGRAMS` (test drivers, helpers) are deliberately excluded.
    """
    decl_re = re.compile(r"^\s*(?:bin|sbin)_PROGRAMS\s*[+:]?=\s*(.*)$")
    out: list[str] = []
    for text in texts:
        # Collapse automake backslash line-continuations first.
        joined = re.sub(r"\\\n", " ", text)
        for line in joined.splitlines():
            m = decl_re.match(line)
            if m:
                out.extend(m.group(1).split())
    return out


def _cmake_installed_executables(texts: Iterable[str]) -> list[str]:
    """Executable target names a CMake project *installs*.

    Collects `add_executable(<name> ...)` declarations and the target list
    of `install(TARGETS ...)`, then returns their intersection so test-only
    executables (declared but not installed) are not picked. Falls back to
    every declared executable when no resolvable install() target list is
    found — still derived from the tree, never a hardcoded list.
    """
    text_list = list(texts)
    exec_names: set[str] = set()
    add_exec_re = re.compile(
        r"add_executable\s*\(\s*([A-Za-z0-9_.-]+)", re.IGNORECASE)
    for text in text_list:
        for m in add_exec_re.finditer(text):
            exec_names.add(m.group(1))
    if not exec_names:
        return []
    install_re = re.compile(
        r"install\s*\(\s*TARGETS\b([^)]*)\)", re.IGNORECASE | re.DOTALL)
    installed: list[str] = []
    for text in text_list:
        for m in install_re.finditer(text):
            for tok in m.group(1).split():
                if tok.upper() in _CMAKE_INSTALL_KEYWORDS:
                    break
                if tok in exec_names:
                    installed.append(tok)
    return installed or sorted(exec_names)


def _meson_executables(texts: Iterable[str]) -> list[str]:
    """First (name) argument of every `executable(...)` call in meson.build."""
    exe_re = re.compile(
        r"\bexecutable\s*\(\s*(?:'([^']+)'|\"([^\"]+)\")", re.IGNORECASE)
    out: list[str] = []
    for text in texts:
        for m in exe_re.finditer(text):
            out.append(m.group(1) or m.group(2))
    return out


def declared_cli_names(target_root: str | os.PathLike, build_system: str) -> list[str]:
    """User-facing CLI program names the target's own build manifests declare.

    Derived at runtime from the live target tree — never a hardcoded
    per-project list, which would couple the shared harness to specific
    codebases under audit. `seed_toml` uses this only to bias its asan_bin
    guess toward the project's installed CLI when build-asan/ holds several
    instrumented executables; the free executable scan remains the fallback
    when this yields nothing (unknown build system, generated names, ...).
    """
    root = Path(target_root)
    if build_system == "autotools":
        raw = _autotools_installed_programs(_manifest_texts(root, "Makefile.am"))
    elif build_system == "cmake":
        raw = _cmake_installed_executables(_manifest_texts(root, "CMakeLists.txt"))
    elif build_system == "meson":
        raw = _meson_executables(_manifest_texts(root, "meson.build"))
    else:
        raw = []
    names: list[str] = []
    seen: set[str] = set()
    for tok in raw:
        # Strip autotools $(EXEEXT) adornment and surrounding quotes.
        tok = tok.replace("$(EXEEXT)", "").strip().strip("'\"")
        if not tok or tok in seen:
            continue
        # Reject unresolved build-system variable references and anything
        # that is not a plain program name.
        if not re.fullmatch(r"[A-Za-z0-9._-]+", tok):
            continue
        seen.add(tok)
        names.append(tok)
    return names

# ─── S4 differential pair taxonomy ──────────────────────────────────
#
# Per-engine CLI flag sets for differential JIT execution. Each engine has
# its own toggle vocabulary; flag names are years-stable so per-target
# metadata in target.toml is the right home. seed_toml writes these
